@pytest.mark.parametrize("word", PROFANE)
def test_profane(word):
    assert det.check(word) is not None, f"ПРОПУЩЕН мат: {word}"


@pytest.mark.parametrize("word", SAFE)
def test_safe(word):
    m = det.check(word)
    assert m is None, f"ЛОЖНОЕ срабатывание: {word} ({m})"


def test_fuzzy_catches_asr_distortion():
    # искажённые ASR формы, неизвестные словарю
    assert det.check("блать") is not None
    assert det.check("пиздц") is not None


def test_extra_words_and_whitelist():
    d = ProfanityDetector(extra_words={"сука"}, whitelist={"бля"})
    assert d.check("сука") is not None
    assert d.check("Сука") is not None
    assert d.check("бля") is None


def test_extra_words_morphology_aware():
    # «дебил» в списке ловит и словоформы (баг-фикс #13)
    d = ProfanityDetector(extra_words={"дебил"})
    assert d.check("дебил") is not None
    assert d.check("дебилы") is not None
    assert d.check("дебила") is not None
    assert d.check("дебилам") is not None
    m = d.check("дебил")
    assert m.spans == ((0, 5),)              # extra-матч теперь со спаном


def test_whitelist_morphology_aware():
    # исключаем «оскорбление» во всех формах, мат рядом по-прежнему ловится
    d = ProfanityDetector(whitelist={"гондон"})
    assert d.check("гондон") is None
    assert d.check("гондоны") is None        # словоформа тоже исключена
    assert d.check("блять") is not None


